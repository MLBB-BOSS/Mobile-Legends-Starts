def create_inline_table(title: str, headers: list[str], rows: list[list[str]]) -> str:
    """
    ะกัะฒะพััั ASCII-ัะฐะฑะปะธัั ะท ะฒะธะบะพัะธััะฐะฝะฝัะผ rich ัะฐ ะฟะพะฒะตััะฐั ัั ัะบ ัะตะบัั.
    ะะธะบะพัะธััะพะฒัะนัะต <pre></pre> ั ะฟะพะฒัะดะพะผะปะตะฝะฝั Telegram ะดะปั ะทะฑะตัะตะถะตะฝะฝั ัะพัะผะฐััะฒะฐะฝะฝั.
    """
    console = Console(record=True)
    table = Table(
        title=title,
        box=DOUBLE,
        show_header=True,
        header_style="bold cyan",
        style="dim"
    )

    for header in headers:
        table.add_column(header, style="bold yellow", justify="center")

    for row in rows:
        table.add_row(*row)

    console.print(table)
    return console.export_text()

