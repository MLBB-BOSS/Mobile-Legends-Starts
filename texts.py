def create_inline_table(title: str, headers: list[str], rows: list[list[str]]) -> str:
    """
    –°—Ç–≤–æ—Ä—é—î ASCII-—Ç–∞–±–ª–∏—Ü—é –∑ –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è–º rich —Ç–∞ –ø–æ–≤–µ—Ä—Ç–∞—î —ó—ó —è–∫ —Ç–µ–∫—Å—Ç.
    –í–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É–π—Ç–µ <pre></pre> —É –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—ñ Telegram –¥–ª—è –∑–±–µ—Ä–µ–∂–µ–Ω–Ω—è —Ñ–æ—Ä–º–∞—Ç—É–≤–∞–Ω–Ω—è.
    """
    console = Console(record=True)
    table = Table(
        title=title,
        box=DOUBLE,
        show_header=True,
        header_style="bold cyan",
        style="dim"
    )

    for header in headers:
        table.add_column(header, style="bold yellow", justify="center")

    for row in rows:
        table.add_row(*row)

    console.print(table)
    return console.export_text()

