def get_profile_table(username: str, level: int, rating: float, achievements_count: int) -> str:
    console = Console(record=True)
    profile_table = Table(
        title="–í–∞—à –ü—Ä–æ—Ñ—ñ–ª—å",
        box=DOUBLE,
        show_header=True,
        header_style="bold cyan",
        style="dim"
    )
    profile_table.add_column("–ü–∞—Ä–∞–º–µ—Ç—Ä", style="bold yellow", no_wrap=True)
    profile_table.add_column("–ó–Ω–∞—á–µ–Ω–Ω—è", style="magenta", justify="center")

    profile_table.add_row("–Ü–º'—è –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞", username)
    profile_table.add_row("–†—ñ–≤–µ–Ω—å", str(level))
    profile_table.add_row("–†–µ–π—Ç–∏–Ω–≥", f"{rating}")
    profile_table.add_row("–î–æ—Å—è–≥–Ω–µ–Ω–Ω—è", str(achievements_count))

    console.print(profile_table)
    return console.export_text()

