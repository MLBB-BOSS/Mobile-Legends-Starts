def create_menu(
    buttons: List[Union[MenuButton, LanguageButton]],
    placeholder: str = "",
    row_width: int = 2
) -> ReplyKeyboardMarkup:
    """
    –°—Ç–≤–æ—Ä—é—î –∑–≤–∏—á–∞–π–Ω–µ (Reply) –º–µ–Ω—é –∑ –∫–Ω–æ–ø–∫–∞–º–∏ (MenuButton –∞–±–æ LanguageButton).
    """
    # –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞, —â–æ –≤—Å—ñ –∫–Ω–æ–ø–∫–∏ –Ω–∞–ª–µ–∂–∞—Ç—å Enum'–∞–º
    if not all(isinstance(button, (MenuButton, LanguageButton)) for button in buttons):
        logger.error("–°–ø–∏—Å–æ–∫ –∫–Ω–æ–ø–æ–∫ –º—ñ—Å—Ç–∏—Ç—å –Ω–µ–∫–æ—Ä–µ–∫—Ç–Ω—ñ –µ–ª–µ–º–µ–Ω—Ç–∏.")
